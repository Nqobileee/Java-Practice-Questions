export const NDEBELE_QUESTIONS_PER_EXAM = 20;

export interface NdebeleExam {
  id: number;
  name: string;
  questions: NdebeleQuestion[];
  questionCount: number;
}

export function divideIntoNdebeleExams(questions: NdebeleQuestion[]): NdebeleExam[] {
  const exams: NdebeleExam[] = [];
  const totalExams = Math.ceil(questions.length / NDEBELE_QUESTIONS_PER_EXAM);

  for (let i = 0; i < totalExams; i++) {
    const start = i * NDEBELE_QUESTIONS_PER_EXAM;
    const end = start + NDEBELE_QUESTIONS_PER_EXAM;
    const examQuestions = questions.slice(start, end);

    exams.push({
      id: i + 1,
      name: `Isivivinyo ${i + 1}`,
      questions: examQuestions,
      questionCount: examQuestions.length,
    });
  }

  return exams;
}

export function getNdebeleOptions(question: NdebeleQuestion): string[] {
  return [question.A, question.B, question.C, question.D];
}

export function checkNdebeleAnswer(userAnswer: string, correctAnswer: string): boolean {
  return userAnswer === correctAnswer;
}
